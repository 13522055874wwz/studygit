package com.wz.entity;

public class Shop {
    private Integer id;

    private String name;

    private Double yuanprice;

    private Double xianprice;

    private String jieshao;

    private Integer like;

    private Integer shoucang;

    private String pic;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Double getYuanprice() {
		return yuanprice;
	}

	public void setYuanprice(Double yuanprice) {
		this.yuanprice = yuanprice;
	}

	public Double getXianprice() {
		return xianprice;
	}

	public void setXianprice(Double xianprice) {
		this.xianprice = xianprice;
	}

	public String getJieshao() {
		return jieshao;
	}

	public void setJieshao(String jieshao) {
		this.jieshao = jieshao;
	}

	public Integer getLike() {
		return like;
	}

	public void setLike(Integer like) {
		this.like = like;
	}

	public Integer getShoucang() {
		return shoucang;
	}

	public void setShoucang(Integer shoucang) {
		this.shoucang = shoucang;
	}

	public String getPic() {
		return pic;
	}

	public void setPic(String pic) {
		this.pic = pic;
	}
    
    
    
    

    
}